INSTALL_DIR="$PREFIX/gap"

rm -rf "$INSTALL_DIR/pkg"

set +e
# Remove all object files and temporary files.
find . \( \
         -name "*.o" \
      -o -name "*.lo" \
      -o -name "*.la" \
      -o -name "*.lai" \
      -o -name ".libs" \
      -o -name "config.log" \
      -o -name "config.status" \
      -o -name "libtool" \
      \) -exec rm -rf {} \;

echo "done"
set -e

mv pkg $INSTALL_DIR/pkg
